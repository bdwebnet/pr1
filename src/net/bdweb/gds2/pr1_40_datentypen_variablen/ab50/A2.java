package net.bdweb.gds2.pr1_40_datentypen_variablen.ab50;

public class A2 {

	public static void main(String[] args) {
		
		// Short
		short zahl_short = 42;
		System.out.println(zahl_short);

		// Double
		double zahl_double = 42;
		System.out.println(zahl_double);

		// Float
		float zahl_float = 42.0F;
		System.out.println(zahl_float);

	}

}
